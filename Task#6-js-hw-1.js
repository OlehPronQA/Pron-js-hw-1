// 6.  Задано номер дня тижня.
// За заданим номером вивести назву дня тижня використовуючи switch.
// Результат виводити в консоль.

let week = 0;

switch (week) {
  case 1:
    console.log("week = 1");
    break;
  case 2:
    console.log("week = 2");
    break;
  case 3:
    console.log("week = 3");
    break;
  case 4:
    console.log("week = 4");
    break;
  default:
    console.log("there is no such week");
    break;
}
